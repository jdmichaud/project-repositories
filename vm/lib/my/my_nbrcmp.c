#include "my.h"

int	my_nbrcmp(char *nb1, char *nb2)
{
  int	nbr1;
  int	nbr2;
  
  nbr1 = my_atoi(nb1);
  nbr2 = my_atoi(nb2);
  return (nbr1 - nbr2);
}
