#include "eval_expr.h"

unsigned int	is_op(char c)
{
  if (c == '+') 
    return (1);
  if (c == '-')
    return (2);
  if (c == '*')
    return (3);
  if (c == '/')
    return (4);
  if (c == '(')
    return (5);
  if (c == ')')
    return (6);
  if (c == '%')
    return (7);
  return (0);
}
