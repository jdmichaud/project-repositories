import java.util.Vector;
import java.awt.*;
import java.awt.event.*;
import java.awt.geom.*;
import javax.swing.*;

/**
 * the graphic context determines the way of an object to display
 */
public class TGraphicContext
{
    /**
     * the x coordinate
     */
    private int _x;

    /**
     * the y coordinate
     */
    private int _y;

    /**
     * the color to display with
     */
    private Color _color;

    public TGraphicContext (int x, int y, Color color)
    {
	_x = x;
	_y = y;
	_color = color;
    }
    
    public int getX () { return _x; }
    public int getY () { return _y; }
    public Color getClr () { return _color; }
}
